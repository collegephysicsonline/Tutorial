# variable_datatype.py

# This script demonstrates the use of variables and data types in Python

# Integer variable
age = 25
print(f"Age: {age}, Type: {type(age)}")

# Float variable
height = 5.9
print(f"Height: {height}, Type: {type(height)}")

# String variable
name = "John Doe"
print(f"Name: {name}, Type: {type(name)}")

# Boolean variable
is_student = True
print(f"Is Student: {is_student}, Type: {type(is_student)}")

# List variable
fruits = ["apple", "banana", "cherry"]
print(f"Fruits: {fruits}, Type: {type(fruits)}")

# Tuple variable
coordinates = (10.0, 20.0)
print(f"Coordinates: {coordinates}, Type: {type(coordinates)}")

# Dictionary variable
person = {"name": "Alice", "age": 30}
print(f"Person: {person}, Type: {type(person)}")

# Set variable
unique_numbers = {1, 2, 3, 4, 5}
print(f"Unique Numbers: {unique_numbers}, Type: {type(unique_numbers)}")

# NoneType variable
nothing = None
print(f"Nothing: {nothing}, Type: {type(nothing)}")

# Complex number variable
complex_number = 3 + 4j
print(f"Complex Number: {complex_number}, Type: {type(complex_number)}")

# Bytes variable
byte_data = b"Hello"
print(f"Byte Data: {byte_data}, Type: {type(byte_data)}")

# Bytearray variable
byte_array_data = bytearray([1, 2, 3])
print(f"Byte Array Data: {byte_array_data}, Type: {type(byte_array_data)}")

# Range variable
range_data = range(10)
print(f"Range Data: {list(range_data)}, Type: {type(range_data)}")